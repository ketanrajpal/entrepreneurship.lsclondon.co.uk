<section id="eligibility" class="even odd">
  <h1>Eligibility<span></span></h1>
  <p>You are a current student or graduate (alumni) of any campus or centre of the LSC Group.<br>
    <br>
    Your business idea must be your own. Franchises or subsidiaries of existing companies are not eligible.<br>
    <br>
    Your entry conforms to the ethical and moral standards of the LSC Group.<br>
    <br>
    The LSC Group reserves the right to disqualify any entry that we feel infringes the spirit of the competition or exceeds the bounds of social convention. </p>
</section>
<section id="ownership-and-disclosure" class="even">
  <h1>Ownership and disclosure<span></span></h1>
  <p>All entrants own the rights to their ideas.<br>
    <br>
    Entrants must acknowledge any trademarks or copyrighted materials that are incorporated into their entry or presentation. The entrants are responsible for obtaining any necessary permission before incorporating copyright or trademark materials into their submissions.<br>
    <br>
    Once submitted, the LSC Group reserve the right to publish information about the entries in publications and press releases.<br>
    <br>
    Winners of the Entrepreneurship Awards will agree to giving the LSC Group a minority stake in the business or social enterprise in return for the start-up capital funding.</p>
