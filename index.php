<section id="about" class="even">
  <h1>Could you be the next LSC Entrepreneur?<span></span></h1>
  <p>The 2016 LSC Entrepreneurship Awards have been developed to give aspiring LSC entrepreneurs, business owners and social entrepreneurs the opportunity to transform their ideas into reality.<br>
    <br>
    If have always wanted to embark on your own business or social enterprise venture, this is your chance to turn your ideas into commercially viable technologies, products or services. If you believe you have the entrepreneurial aptitude, commercial acumen, vision, ambition and drive to build a successful enterprise, this is the perfect opportunity to make your dream materialise into a reality.</p>
</section>
<section id="who-can-enter" class="even odd">
  <h1>Who can enter<span></span></h1>
  <p>The Entrepreneurship Awards is open to all current students and alumni of the LSC Group in any country who have a realistic and exciting concept of a new business venture, particularly in a developing country.<br>
    <br>
    Winners will be awarded start-up capital funding of up to £25,000 and also receive business support and enterprise mentoring from LSC. In return for the funding, the LSC Group will have a minority stake in the business or enterprise. This stake will vary according to individual projects. The LSC Group will plough all profits from its stake in these businesses and enterprises to support future Entrepreneurship Award projects.<br>
    <br>
    The award will open on 1 July 2016 and close on 31 July 2016. </p>
</section>
<section id="entry-requirements" class="even border">
  <h1>Entry requirements<span></span></h1>
  <p>To enter you must prepare a written business plan, develop a 60 second 'creative' and if shortlisted, make an oral presentation (pitch) to a panel of judges. Submit your business plan and creative to <a href="mailto:entrepreneurship@lsclondon.co.uk">entrepreneurship@lsclondon.co.uk</a> by 31 July 2016.</p>
</section>
<section id="the-business-plan" class="even border">
  <h1>The Business Plan<span></span></h1>
  <p>Your written business plan should be between 4 to 6 sides of A4, font size 12 point and must address the following key aspects:</p>
  <div class="accordion">
    <h3><span class="fa fa-caret-right"></span>Executive Summary</h3>
    <div>A clear, exciting and effective overview of the plan with a statement about the benefits to identified target markets and /or social value.</div>
    <h3><span class="fa fa-caret-right"></span>Customer need/problem</h3>
    <div>Clear and compelling presentation of an identified customer/user problem.</div>
    <h3><span class="fa fa-caret-right"></span>Product/service solution</h3>
    <div>A clear description of the key features and benefits of the product/service.</div>
    <h3><span class="fa fa-caret-right"></span>Market Analysis</h3>
    <div>You need to know your market, customer needs, where they are, how to reach them, etc.</div>
    <h3><span class="fa fa-caret-right"></span>Strategy and Implementation</h3>
    <div>What is your competitive advantage and how are you going to 'go to market'?</div>
    <h3><span class="fa fa-caret-right"></span>Management</h3>
    <div>Who are the members of the team, what are their capabilities/responsibilities?</div>
    <h3><span class="fa fa-caret-right"></span>Financial Plan</h3>
    <div>Include cash flow statement for first four years, break-even analysis, and assumptions.</div>
    <h3><span class="fa fa-caret-right"></span>Operations</h3>
    <div>A clear operating statement of how the business will be managed along with a resource plan of the business.</div>
    <h3><span class="fa fa-caret-right"></span>Sustainability</h3>
    <div>How is the business sustainable (financially and ecologically) and what about succession?</div>
    <h3><span class="fa fa-caret-right"></span>Impact/benefit:</h3>
    <div>State the impact or benefit that your idea will have.</div>
    <h3><span class="fa fa-caret-right"></span>Funding requirements</h3>
    <div>What funding are you seeking from the awards (up to £25,000) and any collateral or external funding you have already obtained (for instance, institutional, angel or other financing).</div>
  </div>
