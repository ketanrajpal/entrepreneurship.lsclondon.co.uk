<section id="the-60-second-creative" class="even border">
  <h1>The 60 second creative<span></span></h1>
  <p>This is an opportunity for you to bring your idea to life and 'tell' us about it through an 'elevator pitch' of no more than 60 seconds in length. You can use a video or an animated power point. Please remember to include your name and ensure that it does not contain any copyright content (material that other people have made).</p>
</section>
<section id="the-presentation" class="even">
  <h1>The presentation<span></span></h1>
  <p>Shortlisted entrants will be invited to present their ideas to the judges. The presentation will allow you to introduce your idea and explain your plan - it will also enable the judges to ask questions and for you to highlight areas of your idea that you think are important.<br>
    <br>
    The presentation will be no more than 10 minutes long and be supported by Power Point or similar. Once you have presented your ideas there will be a further 5 minutes of time allocated for questioning from the judges. To enable a fair competition we will be strict on the timekeeping. </p>
</section>
<section id="judging" class="even odd">
  <h1>Judging<span></span></h1>
  <p>Judging will take place over two rounds:</p>
  <ol>
    <li>Please prepare and submit your business plan and creative to <a href="mailto:entrepreneurship@lsclondon.co.uk">entrepreneurship@lsclondon.co.uk</a> by 31 July 2016.</li>
    <li>Shortlisted entrants (finalists) will be informed by 15 August 2015 and invited to a showcase event at LSC London in early October 2016. Finalists will present their idea to a panel of judges comprising of senior LSC managers and lecturers for a chance to win a capital start up fund of up to £25,000 and also receive business support and enterprise mentoring from the LSC Group.</li>
  </ol>
</section>
<section id="what-are-the-judges-looking-for" class="even odd">
  <h1>What are the judges looking for?<span></span></h1>
  <p>At the centre of the awards are your great ideas for new businesses and new social enterprises. The judges will look at your business plan and assess the idea, its benefits and your capability. The judges will want to know, among other things:</p>
  <ol>
    <li>The inspiration behind your business or business idea.</li>
    <li>What sets your business or business idea apart from others.</li>
    <li>Any innovative aspects of your business or business idea.</li>
    <li>That you have a robust business plan and a focus on the financial performance of your business.</li>
    <li>That you can demonstrate an exciting and original creative that brings your business to life.</li>
    <li>What the future looks like for your business or business idea.</li>
  </ol>
</section>
